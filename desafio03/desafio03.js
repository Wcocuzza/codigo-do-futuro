y = Number(prompt("Digite valor de y:"));
h = Number(prompt("Digite valor de h:"));
t = Number(prompt("Digite valor de t:"));
T = Number(prompt("Digite valor de T:"));
o = Number(prompt("Digite valor de o:"));

resultado = y + 5 * 9 / 7 + (h - t / T + o) - 5 * 100;

alert(`A rotação da terra perante o sistema solar é de: ${resultado}`);
